#!/usr/bin/env node
import fs from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const repoRoot = path.resolve(__dirname, '..');

const DEFAULT_INPUT = path.join(repoRoot, 'data/manual/nativeEffectRules.manual.tsv');
const DEFAULT_OUTPUT = path.join(repoRoot, 'src/data/generated/nativeEffectRules.generated.js');

function parseArgs(argv) {
  const args = { input: DEFAULT_INPUT, output: DEFAULT_OUTPUT };
  for (const arg of argv.slice(2)) {
    if (arg.startsWith('--input=')) args.input = path.resolve(arg.slice('--input='.length));
    else if (arg.startsWith('--output=')) args.output = path.resolve(arg.slice('--output='.length));
    else if (arg === '--help' || arg === '-h') args.help = true;
  }
  return args;
}

function usage() {
  console.log(`Usage: node tools/build-native-effect-rules.mjs [--input=path] [--output=path]\n\nBuilds src/data/generated/nativeEffectRules.generated.js from data/manual/nativeEffectRules.manual.tsv.`);
}

function stripBom(text) {
  return text.replace(/^\uFEFF/, '');
}

function parseTsv(text) {
  const lines = stripBom(text).split(/\r?\n/).filter(line => line.trim() && !line.trim().startsWith('#'));
  if (!lines.length) return [];
  const headers = lines[0].split('\t').map(h => h.trim());
  return lines.slice(1).map((line, idx) => {
    const cells = line.split('\t');
    const row = { __line: idx + 2 };
    headers.forEach((h, i) => {
      row[h] = cells[i] == null ? '' : cells[i].trim();
    });
    return row;
  });
}

function boolish(value) {
  const v = String(value ?? '').trim().toLowerCase();
  return ['1', 'true', 'yes', 'y', 'on', '有効', '○'].includes(v);
}

function splitList(value) {
  return String(value ?? '')
    .split('|')
    .map(v => v.trim())
    .filter(Boolean);
}

function parseJsonCell(value, fallback, label, line) {
  const text = String(value ?? '').trim();
  if (!text) return fallback;
  try {
    return JSON.parse(text);
  } catch (error) {
    throw new Error(`${label} JSON parse failed at line ${line}: ${error.message}\n${text}`);
  }
}

function normalizeName(value) {
  return String(value ?? '')
    .normalize('NFKC')
    .replace(/\s+/g, ' ')
    .trim();
}

function normalizeRule(row) {
  const id = String(row.id || '').trim();
  if (!id) throw new Error(`Missing id at line ${row.__line}`);

  const targetName = normalizeName(row.target_name);
  const equipmentNames = splitList(row.equipment_names).map(normalizeName);
  const effects = parseJsonCell(row.effects_json, {}, 'effects_json', row.__line);
  const unsupported = parseJsonCell(row.unsupported_json, {}, 'unsupported_json', row.__line);

  return {
    id,
    enabled: boolish(row.enabled),
    category: String(row.category || '').trim() || 'equipment_buff',
    matchType: String(row.match_type || '').trim() || 'buff_name',
    targetName,
    normalizedTargetName: normalizeName(targetName),
    equipmentNames,
    normalizedEquipmentNames: equipmentNames.map(normalizeName),
    technicId: row.technic_id ? Number(row.technic_id) : null,
    effects,
    unsupported,
    stackGroup: String(row.stack_group || '').trim(),
    stackMode: String(row.stack_mode || '').trim() || 'add',
    verification: String(row.verification || '').trim() || 'manual',
    notes: String(row.notes || '').trim(),
    sources: splitList(row.sources),
  };
}

function buildIndexes(rules) {
  const byBuffName = {};
  const byEquipmentName = {};
  const byTechnicId = {};

  for (const rule of rules) {
    if (!rule.enabled) continue;
    if (rule.normalizedTargetName) {
      (byBuffName[rule.normalizedTargetName] ||= []).push(rule.id);
    }
    for (const name of rule.normalizedEquipmentNames || []) {
      (byEquipmentName[name] ||= []).push(rule.id);
    }
    if (rule.technicId != null && !Number.isNaN(rule.technicId)) {
      (byTechnicId[String(rule.technicId)] ||= []).push(rule.id);
    }
  }

  return { byBuffName, byEquipmentName, byTechnicId };
}

function jsonForJs(value) {
  return JSON.stringify(value, null, 2).replace(/<\/script/gi, '<\\/script');
}

async function main() {
  const args = parseArgs(process.argv);
  if (args.help) {
    usage();
    return;
  }

  const text = await fs.readFile(args.input, 'utf8');
  const rows = parseTsv(text);
  const rules = rows.map(normalizeRule);
  const indexes = buildIndexes(rules);
  const meta = {
    generatedAt: new Date().toISOString(),
    source: path.relative(repoRoot, args.input).replace(/\\/g, '/'),
    ruleCount: rules.length,
    enabledRuleCount: rules.filter(r => r.enabled).length,
    policy: 'native-manual-rules; external extracted catalogs are not used as source of truth',
  };

  const body = `// Generated by tools/build-native-effect-rules.mjs\n// ${meta.generatedAt}\n// Source: ${meta.source}\n(function(){\n  const meta = ${jsonForJs(meta)};\n  const rules = ${jsonForJs(rules)};\n  const indexes = ${jsonForJs(indexes)};\n\n  window.MOE_NATIVE_EFFECT_RULES_META = meta;\n  window.MOE_NATIVE_EFFECT_RULES = rules;\n  window.MOE_NATIVE_EFFECT_RULE_INDEXES = indexes;\n})();\n`;

  await fs.mkdir(path.dirname(args.output), { recursive: true });
  await fs.writeFile(args.output, body, 'utf8');
  console.log(`[native-effect-rules] wrote ${path.relative(repoRoot, args.output)} (${rules.length} rules, ${meta.enabledRuleCount} enabled)`);
}

main().catch(error => {
  console.error(error && error.stack || error);
  process.exitCode = 1;
});
